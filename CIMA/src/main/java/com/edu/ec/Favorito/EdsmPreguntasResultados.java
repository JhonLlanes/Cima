package com.edu.ec.Favorito;

public class EdsmPreguntasResultados {

	private String nivel;
	private String area;
	private String categoria_pregunta;
	private String pregunta;
	private String observador;
	private String padre;
	private String valor;
	public String getPregunta() {
		return pregunta;
	}
	public void setPregunta(String pregunta) {
		this.pregunta = pregunta;
	}
	public String getObservador() {
		return observador;
	}
	public void setObservador(String observador) {
		this.observador = observador;
	}
	public String getPadre() {
		return padre;
	}
	public void setPadre(String padre) {
		this.padre = padre;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	public String getCategoria_pregunta() {
		return categoria_pregunta;
	}
	public void setCategoria_pregunta(String categoria_pregunta) {
		this.categoria_pregunta = categoria_pregunta;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	
	
}
